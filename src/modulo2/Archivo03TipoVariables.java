package modulo2;

public class Archivo03TipoVariables {
    public static void main(String[] args) {
        int precioNaranja = 5;
        System.out.println("Precio de la naranja: " + precioNaranja);

        double precioManzana = 6.25;
        System.out.println("Precio de la manzana: " + precioManzana);

        String nombre = "Juanito Perez";
        System.out.println("Hola " + nombre);

        String precioUvas = "$5.5";
        double precioUvas2 = 5.5;
        System.out.println("Precio de las uvas: " + precioUvas);
        System.out.println("Precio de las uvas: " + precioUvas2);
    }
}

package modulo3;

public class Archivo03OperadoresIncrementoDecremento {
    public static void main(String[] args) {
        double contador = 1;
        System.out.println("Contenido contador: " + contador);

        contador++;
        contador++;
        System.out.println("Contenido contador: " + contador);

        contador--;
        System.out.println("Contenido contador: " + contador);

    }
}
